"""
The Python test program to validate correctness of `from-gray.asm` and `to-gray.asm`.
"""

def binary_to_gray(binary_):
    return binary_ ^ (binary_ >> 1)

def gray_to_binary(gray):
    binary_ = gray
    while gray > 0:
        gray >>= 1
        binary_ ^= gray
    return binary_

for i in range(128):
    if i%16==0:
        print("N__\tGray___\tBinary_")

    gray_code = binary_to_gray(i)
    binary = gray_to_binary(gray_code)
    print(f"{format(i, '3d')}\t{format(gray_code, '7b')}\t{format(binary, '7b')}")

