; ------------------------------------------------------
; Unipolar Motor, Full-Step Drive, Clockwise
; OUT0 = stepper contact A
; OUT1 = stepper contact B
; OUT2 = stepper contact A'
; OUT3 = stepper contact B'
; ------------------------------------------------------
; Using a full-step drive, this program drives a unipolar stepper motor clockwise.
;
;    +----++----+            +----++----+            +----++----+
;    |          |            |          |            |          |
; A ++          ++----++----++          ++----++----++          +
;          +----++----+            +----++----+            +----+
;          |          |            |          |            |
; B ++----++          ++----++----++          ++----++----++
;                +----++----+            +----++----+
;                |          |            |          |
; A'++----++----++          ++----++----++          ++----++----+
;   ++----+            +----++----+            +----++----+
;         |            |          |            |          |
; B'      ++----++----++          ++----++----++          ++----+
;
; ------------------------------------------------------


; ------------------------------------------
; Memory and IO mapping for the MC14500 CPU
; ------------------------------------------

; ------------------------------------------
; Inputs
; ------------------------------------------
RR      EQU     0x00  ; Pin RR is wired to input 0

; ------------------------------------------
; Outputs
; ------------------------------------------
A       EQU     0x00
B       EQU     0x01
A_PRIME EQU     0x02
B_PRIME EQU     0x03
PiN1    EQU     A
PiN2    EQU     B
PiN3    EQU     A_PRIME
PiN4    EQU     B_PRIME

FULL_STEP
